# ***************************************************************
# Custom aliases for my shell environment, including:
# - rm, cp, mv, mkdir
# - cd
# ***************************************************************

alias rm='rm -i'
alias cp='cp -i'
alias mv='mv -i'
alias mkdir='mkdir -p'
alias ..='cd ..'
alias ...='cd ../..'
alias ....='cd ../../..'
alias ccd='clear && cd'
